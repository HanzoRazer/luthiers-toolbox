<script setup lang="ts">
/**
 * DiagnosticSnapshotExportMetadataCard — Export metadata display
 *
 * Dev Order 41: Displays snapshot export package metadata.
 *
 * Observational only — does NOT track download events, persist state,
 * or indicate actual export occurred.
 */
import type { DiagnosticSnapshotExportMetadata } from '@/types/diagnosticSnapshot'

const props = defineProps<{
  metadata: DiagnosticSnapshotExportMetadata
  warnings?: string[]
}>()

function formatTimestamp(iso: string): string {
  const date = new Date(iso)
  if (isNaN(date.getTime())) return 'Invalid'
  return date.toLocaleString()
}

function formatExportStatus(status: string): string {
  switch (status) {
    case 'prepared_not_exported':
      return 'Prepared (not exported)'
    case 'exported_json':
      return 'Exported as JSON'
    default:
      return status
  }
}
</script>

